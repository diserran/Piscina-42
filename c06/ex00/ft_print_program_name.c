#include <unistd.h>

void	ft_putstr(char *str);

int	main(int argc, char *argv[])
{
	if (argc)
	{
		ft_putstr(argv[0]);
		ft_putstr("\n");
	}
}

void	ft_putstr(char *str)
{
	int	i;

	i = -1;
	while (str[++i] != '\0')
		write(1, &str[i], 1);
}
